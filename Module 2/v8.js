
const sales = [
    { category: "electronics", item: "laptop", price: 800, quantity: 4 },
    { category: "electronics", item: "phone", price: 500, quantity: 6 },
    { category: "clothing", item: "t-shirt", price: 20, quantity: 10 },
    { category: "clothing", item: "jeans", price: 40, quantity: 5 },
    { category: "grocery", item: "rice", price: 30, quantity: 8 }
];


const totalSalesByCategory = sales.reduce((table, sale) => {
   const {category, price , quantity} = sale 
   // destructring 

   if(!table[category]){ // if not availabel then initialization
        table[category] = {
           totalRevenue: 0,
           itemCount: 0,
       }
   }

    table[category].totalRevenue += price * quantity
    table[category].itemCount += quantity

   return table

}, {})


