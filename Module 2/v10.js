// ? Binning (resampling) time series Data

/// scenario : you have a long list of user click events
/// you need to "bin" these events into 30 minute intervals and count them to see engagemnt over time

/// ? input 

const events = [
    { timestamp: "2026-10-22T10:01:00Z", type: "click" },
    { timestamp: "2026-10-22T10:03:12Z", type: "scroll" },
    { timestamp: "2026-10-22T10:05:45Z", type: "hover" },
    { timestamp: "2026-10-22T10:07:30Z", type: "click" },
    { timestamp: "2026-10-22T10:10:05Z", type: "input" }
];

const INTERVAL  = 30*60*1000 // 30 min in ms

const getBinningTimeStam = (timestamp) => {

    const date = new Date(timestamp)

    // console.log(date.getTime()) 


    const flooredDate = Math.floor(date.getTime() / INTERVAL) * INTERVAL
    // console.log(flooredDate)

    return new Date(flooredDate).toISOString()
}

// getBinningTimeStam("2026-10-22T10:03:12Z")

// console.log(getBinningTimeStam("2026-10-22T10:03:12Z"))


const binnedData = events.reduce((acc, event) => {
    const bin = getBinningTimeStam(event.timestamp)

    if(!acc[bin]){
        acc[bin] = { total : 0}
    }

    acc[bin].total = acc[bin].total + 1

    return acc
} , {})


