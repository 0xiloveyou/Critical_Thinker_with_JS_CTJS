// Scenarion : you have an array of users and a separate array oif posts 
/// you wnat to create a new array of users where wach user object contains a posts array of their own posts

/// ? input 

const users = [
    { id: 101, name: "Alice" },
    { id: 102, name: "Bob" },
    { id: 103, name: "Charlie" },
    { id: 104, name: "David" },
    { id: 105, name: "Eve" }
];

const posts = [
    { id: 1, userId: 101, title: "Alice's first post" },
    { id: 2, userId: 102, title: "Bob's thoughts" },
    { id: 3, userId: 103, title: "Charlie's story" },
    { id: 4, userId: 104, title: "David's update" },
    { id: 5, userId: 105, title: "Eve's blog" }
];

/// lookup table making 
const postByUserId = posts.reduce((table, post) => {

    const {userId} = post

    if(!table[userId]){
        table[userId] = []
    }

    table[userId].push(post)

    return table

}, {})

// console.log(postByUserId)

const userWithPosts = users.map((user) => {
    return {
        ...user , posts : postByUserId[user.id] || []
    }
})

/// reduce time com to O(n)

console.log(JSON.stringify(userWithPosts))


